#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn task_queue_push_pop() {
        let mut q = TaskQueue::new();
        let id1 = q.push("first".into(), TaskOrigin::User);
        let id2 = q.push("second".into(), TaskOrigin::User);
        assert_ne!(id1, id2);
        assert_eq!(q.len(), 2);

        let t = q.pop_front().unwrap();
        assert_eq!(t.name, "first");
        assert_eq!(q.len(), 1);
    }

    #[test]
    fn task_queue_replace() {
        let mut q = TaskQueue::new();
        q.push("a".into(), TaskOrigin::User);
        q.push("b".into(), TaskOrigin::User);

        let new_tasks = vec![
            BrainTask { id: 10, name: "x".into(), origin: TaskOrigin::User, result: None },
            BrainTask { id: 11, name: "y".into(), origin: TaskOrigin::User, result: None },
        ];
        q.replace(new_tasks);

        assert_eq!(q.len(), 2);
        assert_eq!(q.pop_front().unwrap().name, "x");
    }

    #[test]
    fn parse_task_list_numbered() {
        let response = "1. Fix the compiler error\n2. Run tests\n3. Update docs\n";
        let tasks = parse_task_list(response);
        assert_eq!(tasks, vec!["Fix the compiler error", "Run tests", "Update docs"]);
    }

    #[test]
    fn parse_task_list_no_new_tasks() {
        let response = "No new tasks.";
        let tasks = parse_task_list(response);
        assert!(tasks.is_empty());
    }

    #[test]
    fn parse_task_list_ignores_non_numbered() {
        let response = "Here are some tasks:\n1. First\nNot a task\n2. Second\n";
        let tasks = parse_task_list(response);
        assert_eq!(tasks, vec!["First", "Second"]);
    }

    #[test]
    fn goal_pursuit_lifecycle() {
        let mut gp = GoalPursuit::new();
        assert!(!gp.is_active());

        gp.set_objective("fix all bugs".into(), "run cargo test".into());
        assert!(gp.is_active());
        assert!(!gp.should_stop());

        let task = gp.queue.pop_front().unwrap();
        assert_eq!(task.name, "run cargo test");

        gp.record_completion(task, TaskResult {
            data: "3 tests failed".into(),
            success: false,
            timestamp_secs: 0,
        });

        assert_eq!(gp.current_cycle, 1);
        assert!(gp.queue.is_empty()); // No new tasks yet
    }

    #[test]
    fn reflexion_memory_sliding_window() {
        let mut mem = ReflexionMemory::new("sig".into(), "fix bug".into());
        mem.add_reflection("tried A, failed".into());
        mem.add_reflection("tried B, failed".into());
        mem.add_reflection("tried C, failed".into());
        mem.add_reflection("tried D, failed".into());

        // Should only keep last 3.
        assert_eq!(mem.reflections.len(), 3);
        assert!(mem.reflections[0].text.contains("B"));
        assert!(mem.reflections[1].text.contains("C"));
        assert!(mem.reflections[2].text.contains("D"));
    }

    #[test]
    fn reflexion_inject_into_prompt() {
        let mut mem = ReflexionMemory::new("sig".into(), "fix bug".into());
        mem.add_reflection("Don't forget to run tests".into());

        let prompt = inject_reflections("Base prompt.", "Fix the bug", &mem);
        assert!(prompt.contains("Base prompt."));
        assert!(prompt.contains("Lessons from past attempts"));
        assert!(prompt.contains("Don't forget to run tests"));
        assert!(prompt.contains("Fix the bug"));
    }

    #[test]
    fn reflection_prompt_includes_trajectory() {
        let prompt = build_reflection_prompt(
            "> cargo test\n3 failures\n",
            &["tried fixing imports"],
        );
        assert!(prompt.contains("cargo test"));
        assert!(prompt.contains("3 failures"));
        assert!(prompt.contains("tried fixing imports"));
    }

    #[test]
    fn goal_pursuit_summary() {
        let mut gp = GoalPursuit::new();
        gp.set_objective("ship v1".into(), "build".into());
        let s = gp.summary();
        assert!(s.contains("ship v1"));
        assert!(s.contains("0 done"));
        assert!(s.contains("1 remaining"));
    }
}
